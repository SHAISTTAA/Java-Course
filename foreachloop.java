public class foreachloop {
    public static void main(String args []){
        int age [] = {12, 13, 14, 15, 16};

        for (int eachage : age){
            System.out.println("The age is : " + eachage);
        }
        String name[] = {"John", "Jane", "Doe"};
        for (String n : name) {
            System.out.println("The name is : " + n);
        }
     }
    
}

public class AssignmentOperator {
    public static void main(String[] args) {
        int a = 10; // Using '=' operator for assignment
        a += 5;     // Using '+=' operator to add and assign
        a -= 3;     // Using '-=' operator to subtract and assign
        a *= 2;     // Using '*=' operator to multiply and assign
        a /= 4;     // Using '/=' operator to divide and assign
        a %= 3;     // Using '%=' operator to get remainder and assign

        System.out.println("Final value of a: " + a);
    }
}